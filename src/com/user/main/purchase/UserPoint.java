package com.user.main.purchase;

public class UserPoint {
	int point_serial_id;
	long serial_number;
	int point;
	int discount_type_id;
	
	public int getPoint_serial_id() {
		return point_serial_id;
	}
	
	public void setPoint_serial_id(int point_serial_id) {
		this.point_serial_id = point_serial_id;
	}
	
	public long getSerial_number() {
		return serial_number;
	}
	
	public void setSerial_number(long serial_number) {
		this.serial_number = serial_number;
	}
	
	public int getPoint() {
		return point;
	}
	
	public void setPoint(int point) {
		this.point = point;
	}

	public int getDiscount_type_id() {
		return discount_type_id;
	}

	public void setDiscount_type_id(int discount_type_id) {
		this.discount_type_id = discount_type_id;
	}
	
	
}
