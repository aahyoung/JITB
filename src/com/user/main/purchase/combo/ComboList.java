package com.user.main.purchase.combo;

public class ComboList {
	int combo_list_id;
	int amount;
	int size_id;
	String size;
	String top_opt_name;
	String top_opt_img;
	
	public int getCombo_list_id() {
		return combo_list_id;
	}
	
	public void setCombo_list_id(int combo_list_id) {
		this.combo_list_id = combo_list_id;
	}
	
	public int getAmount() {
		return amount;
	}
	
	public void setAmount(int amount) {
		this.amount = amount;
	}
	
	public int getSize_id() {
		return size_id;
	}
	
	public void setSize_id(int size_id) {
		this.size_id = size_id;
	}
	
	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getTop_opt_name() {
		return top_opt_name;
	}
	
	public void setTop_opt_name(String top_opt_name) {
		this.top_opt_name = top_opt_name;
	}
	
	public String getTop_opt_img() {
		return top_opt_img;
	}
	
	public void setTop_opt_img(String top_opt_img) {
		this.top_opt_img = top_opt_img;
	}
}
